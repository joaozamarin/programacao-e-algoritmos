// Elabore um algoritmo que leia 3 números e apresente o resultado da soma do primeiro pelo segundo multiplicado pelo terceiro. //

// Entrada
var num1 = parseInt(prompt("Digite um número"));
var num2 = parseInt(prompt("Digite outro número"));
var num3 = parseInt(prompt("Digite outro número"))

// Processamento
var resultado = (num1 + num2) * num3;

// Saída
alert("A soma do primeiro pelo segundo multiplicado pelo terceiro é igual a " + resultado);